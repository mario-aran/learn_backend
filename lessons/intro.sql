-- ¿Que es SQL?
- Es un "Lenguaje de Consulta Estructurado", el cual te permite acceder y manipular bases de datos.

-- ¿Qué es una base de datos?
- Es un conjunto de datos organizados y estructurados en una o multiples tablas.

-- ¿Qué es una tabla?
- Es una matríz de registros, como las tablas de excel con filas y columnas.

-- ¿Cómo utilizo SQL?
- Instalando un RDBMS o Sistema de Gestión de Bases de Datos Relacionales
- Ej RDBMS pago: "Microsoft SQL Server", "Oracle", "SAP Hana". 
- Ej RDBMS gratis: "PostreSQL", "SQLite", "MySQL/MariaDB".
- Usaremos "PostgreSQL".

-- Cómo interactuo con SQL
- Con un cliente de SQL
- Ej: "DBeaver", "pgAdmin", "SQL Server Management Studio", "MySQL Workbench". 
- Usaremos "DBeaver"

